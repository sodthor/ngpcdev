	public static final int RESERVED = 420;
	
	public static int[] set_tile = new int[RESERVED];
	public static int[] set_level = new int[BLOCKS_TILES_COUNT];

	public static int[] SCROLL_PLANE_1 = new int[32*32];
	public static int[] SCROLL_PLANE_2 = new int[32*32];
	
	public static int SCR1_X = 0;
	public static int SCR1_Y = 0;
	public static int SCR2_X = 0;
	public static int SCR2_Y = 0;
	
	static int maxcount = 0;
	static int maxmove = 0;
	
	public static void setXY(int x, int y) {
		int[] used = new int[BLOCKS_TILES_COUNT];
		Arrays.fill(used, 0);
		// check used tiles
		for (int i = 0; i < 21; ++i) {
			for (int j = 0; j < 20; ++j) {
				int t = TEST[x+i][y+j];
				if (t >= 0) {
					used[BLOCKS_IDX1[t&0xfff]] = 1;
					used[BLOCKS_IDX2[t&0xfff]] = 1;
				}
			}
		}
		
		// check overload
		int count = 0;
		for (int i = 0; i < BLOCKS_TILES_COUNT; ++i) {
			count += used[i];
		}
		if (count > RESERVED) {
			throw new RuntimeException();
		}
		// free used
		for (int i = 0; i < BLOCKS_TILES_COUNT; ++i) {
			if (used[i]==0) {
				if (set_level[i]>=0) {
					set_tile[set_level[i]] = -1;
					set_level[i] = -1;
				}
			}
		}
		// load new tiles
		int last = 0;
		int move = 0;
		for (int i = 0; i < BLOCKS_TILES_COUNT; ++i) {
			if (used[i]==1 && set_level[i]==-1) {
				while (set_tile[last] != -1)
					last+=1;
				set_tile[last] = i;
				set_level[i] = last;
				// load tile i to last
				move+=1;
			}
		}
		// stats
		if (move>maxmove && maxcount>0) {
			maxmove=move;
		}
		if (count > maxcount) {
			maxcount = count;
		}
	}

	public static void main(String[] args) {
		Arrays.fill(set_tile, -1);
		Arrays.fill(set_level, -1);
		for (int x = 0; x < TEST_WIDTH - 21; ++x)
		{
			for (int y = 0; y < TEST_HEIGHT - 20; ++y)
				setXY(x,y);
//			x += 1;
			for (int y = TEST_HEIGHT - 20; y >= 0; --y)
				setXY(x,y);
		}
		System.err.println("max count:"+maxcount);
		System.err.println("max move:"+maxmove);
	}
